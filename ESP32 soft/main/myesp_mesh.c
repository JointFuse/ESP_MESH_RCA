void app_main()
{
    /* enable some IO pins to protect through connections */
    gpio_config_t gpio_settings = {
        .intr_type = GPIO_INTR_DISABLE,
        .mode = GPIO_MODE_INPUT,
        .pin_bit_mask = GPIO_PROTECT_PULL,
        .pull_down_en = 0,
        .pull_up_en = 0
    };
    gpio_config(&gpio_settings);

    ESP_ERROR_CHECK(nvs_flash_init());
    /*  tcpip initialization */
    ESP_ERROR_CHECK(esp_netif_init());
    /*  event initialization */
    ESP_ERROR_CHECK(esp_event_loop_create_default());
    /*  create network interfaces for mesh (only station instance saved for further manipulation, soft AP instance ignored */
    ESP_ERROR_CHECK(esp_netif_create_default_wifi_mesh_netifs(&netif_sta, NULL));
    /*  wifi initialization */
    wifi_init_config_t config = WIFI_INIT_CONFIG_DEFAULT();
    ESP_ERROR_CHECK(esp_wifi_init(&config));
    ESP_ERROR_CHECK(esp_event_handler_register(IP_EVENT, IP_EVENT_STA_GOT_IP, &ip_event_handler, NULL));
    /* Event registration for smartconfig */
    smartconfig_get_router_settings(&mesh_wifi_event_group);
    assert(*mesh_wifi_event_group);
    /* wifi init second half */
    ESP_ERROR_CHECK(esp_wifi_set_storage(WIFI_STORAGE_FLASH));
    ESP_ERROR_CHECK(esp_wifi_start());
    /*  mesh initialization */
    ESP_ERROR_CHECK(esp_mesh_init());
    ESP_ERROR_CHECK(esp_event_handler_register(MESH_EVENT, ESP_EVENT_ANY_ID, &mesh_event_handler, NULL));
    /*  set mesh topology */
    ESP_ERROR_CHECK(esp_mesh_set_topology(CONFIG_MESH_TOPOLOGY));
    /*  set mesh max layer according to the topology */
    ESP_ERROR_CHECK(esp_mesh_set_max_layer(CONFIG_MESH_MAX_LAYER));
    ESP_ERROR_CHECK(esp_mesh_set_vote_percentage(1));
    ESP_ERROR_CHECK(esp_mesh_set_xon_qsize(128));
    /* Disable mesh PS function */
    ESP_ERROR_CHECK(esp_mesh_disable_ps());
    ESP_ERROR_CHECK(esp_mesh_set_ap_assoc_expire(10));
    /* Mail interface initialization */
    ESP_ERROR_CHECK(mail_socket_init());
    /* MCPWM initialization and activation */
    ESP_ERROR_CHECK(mcpwm_module_init());
    ESP_ERROR_CHECK(mcpwm_module_start());
    /* LED indicator init */
    ESP_ERROR_CHECK(led_indicate_init());
    led_indicate_red_blink();

    xTaskCreate(mesh_config_task, "mesh_config_task", 4096, NULL, 3, NULL);
    ESP_LOGI(MESH_TAG, "<initialized> mesh ready to configure");
}

static void mesh_config_task(void* param)
{
    EventBits_t uxBits;

    mesh_cfg_t cfg = MESH_INIT_CONFIG_DEFAULT();
    /* mesh ID */
    memcpy((uint8_t*)&cfg.mesh_id, MESH_ID, 6);
    /* mesh softAP */
    ESP_ERROR_CHECK(esp_mesh_set_ap_authmode(CONFIG_MESH_AP_AUTHMODE));
    cfg.mesh_ap.max_connection = CONFIG_MESH_AP_CONNECTIONS;
    cfg.mesh_ap.nonmesh_max_connection = CONFIG_MESH_NON_MESH_AP_CONNECTIONS;
    memcpy((uint8_t*)&cfg.mesh_ap.password, CONFIG_MESH_AP_PASSWD,
        strlen(CONFIG_MESH_AP_PASSWD));
 
    while (1) {
        /* check whether smartconf end */
        uxBits = xEventGroupWaitBits(*mesh_wifi_event_group, ESPTOUCH_GOTCONF, true, false, portMAX_DELAY);
        if (!(uxBits & ESPTOUCH_GOTCONF))
            continue;
        /* get wifi router configuration */
        wifi_config_t wifi_cfg;
        ESP_ERROR_CHECK(esp_wifi_get_config(WIFI_IF_STA, &wifi_cfg));
        /* router */
        cfg.channel = wifi_cfg.sta.channel;
        cfg.router.ssid_len = sizeof(wifi_cfg.sta.ssid);
        memcpy((uint8_t*)&cfg.router.ssid, wifi_cfg.sta.ssid, cfg.router.ssid_len);
        memcpy((uint8_t*)&cfg.router.password, wifi_cfg.sta.password, sizeof(wifi_cfg.sta.password));
        ESP_ERROR_CHECK(esp_mesh_set_config(&cfg));
        /* mesh start */
        ESP_ERROR_CHECK(esp_mesh_start());
        ESP_LOGI(MESH_TAG, "<started>heap:%d, %s<%d>%s, ps:%d\n", esp_get_minimum_free_heap_size(),
            esp_mesh_is_root_fixed() ? "root fixed" : "root not fixed",
            esp_mesh_get_topology(), esp_mesh_get_topology() ? "(chain)" : "(tree)", esp_mesh_is_ps_enabled());
        vTaskDelete(NULL);
    }
}

static void serv_activate_task(void* param)
{
    /* WIFI state indicators */
    EventBits_t uxBits;

    while (1) {
        /* check whether smartconf end */
        uxBits = xEventGroupWaitBits(*mesh_wifi_event_group, ESPTOUCH_DONE_BIT, true, false, portMAX_DELAY);
        if (!(uxBits & ESPTOUCH_DONE_BIT))
            continue;
        /* push ASIO server if all wifi things over */
        led_indicate_green_blink();
        asioserver_start();
        vTaskDelete(NULL);
    }
}